<h2>Fees</h2>
<p>TuksFencing membership fees for 2012 are R 400.00 for seniors and R 250.00 for juniors.</p>
<p>Membership forms can be filled in and fees can be paid at the TuksSport Reception/Information desk in the Sport Centre.</p>
The membership fee includes the following:
<ul>
	<li>Affiliation to TuksFencing</li>
	<li>Access to train in the facility</li>
	<li>Tuks T-Shirt, socks or a beanie</li>
</ul>

<p>Additional fees that will be incurred are coaching fees, these are to be negotiated with the coaches.</p>



